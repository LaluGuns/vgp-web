export function jaKoMarketRouteCopy(locale: string, path: string): JaKoMarketRouteCopy | undefined {
  if (locale === "ja-JP") return ja[path] ?? (path.startsWith("alternatives/") ? genericAlternative(locale, path) : undefined);
  if (locale === "ko-KR") return ko[path] ?? (path.startsWith("alternatives/") ? genericAlternative(locale, path) : undefined);
  return undefined;
}
